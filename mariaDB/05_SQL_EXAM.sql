-- 문제 시작
-- 1번
SELECT COMPANY, CONVERT(CONCAT(COUNT(1), '개') USING UTF8) 'COUNT'
FROM NOODLE
GROUP BY COMPANY;

-- 2번
SELECT COMPANY, CAST(CONCAT(COUNT(1), '개') AS VARCHAR(10)) 'COUNT'
FROM NOODLE
GROUP BY COMPANY
ORDER BY COUNT(1) ASC;

-- 3번
SELECT COMPANY, CONVERT(CONCAT(COUNT(1), '개') USING UTF8) 'COUNT'
FROM NOODLE
GROUP BY COMPANY
HAVING COUNT(1) >= 3;

-- 4번
SELECT NAME, COMPANY, CONVERT(DATE_FORMAT(E_DATE, '%Y년 %m월 %d일') USING UTF8) EXP
FROM NOODLE
WHERE KIND = 'M' AND PRICE >= 800;

-- 5번
SELECT CAST(CONCAT(MAX(PRICE), '원') AS VARCHAR(10)) MAXPRICE
FROM NOODLE
WHERE KIND = 'C';

-- 6번
SELECT COMPANY, MIN(PRICE)
FROM NOODLE
GROUP BY COMPANY;

-- 7번
SELECT COMPANY, SUM(PRICE)
FROM NOODLE
GROUP BY COMPANY
ORDER BY SUM(PRICE);

-- 8번
SELECT COMPANY, ROUND(AVG(PRICE), 0)
FROM NOODLE
GROUP BY COMPANY
ORDER BY AVG(PRICE) DESC;