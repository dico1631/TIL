SELECT WEEK, SUM(DIE) 교통사고수
FROM ACCIDENT
GROUP BY WEEK;

-- 2번
SELECT AREA1, SUM(DIE)
FROM ACCIDENT
GROUP BY AREA1;

-- 3번
SELECT AREA1, AREA2, SUM(DIE)
FROM ACCIDENT
GROUP BY AREA1, AREA2;

-- 4번
SELECT KIND, COUNT(*)
FROM ACCIDENT
GROUP BY KIND
ORDER BY COUNT(*) DESC
LIMIT 0, 5;

-- 5번
SELECT WEEK, COUNT(*)
FROM ACCIDENT
GROUP BY WEEK
ORDER BY COUNT(*)
LIMIT 1;
